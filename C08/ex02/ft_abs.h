#ifndef FT_ABS_H
# define FT_ABS_H

# define ABS(nbr) ({ \
					int res; \
					if( (nbr) < 0){ \
				   			res = -(nbr); \
					} else { \
							res = (nbr); \
					} \
					res; \
})

#endif

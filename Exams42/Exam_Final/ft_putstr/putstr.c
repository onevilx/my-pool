#include <unistd.h>
char	putstr(char *str)
{
	int	i;

	i = 0;
	while(str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	return str[i];
}
int	main()
{
	putstr("hihihaxor\n");
}
